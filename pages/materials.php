                            Основная задача Центра &ndash; обеспечить своим подопечным достойный уровень жизни, сохранить основные когнитивные функции (память, мышление, внимание, речь), а также поддержать их семьи.<br />
                            <br />
                            В Центре&nbsp; для подопечных организуется продуктивная дневная занятость и социальная реабилитация, присмотр и уход с целью формирования навыков самостоятельного проживания. Здесь квалифицированными педагогами и психологами проводятся занятия по адаптивной физической культуре, творчеству и психологической релаксации. Это способствует уменьшению изоляции, чувства одиночества и улучшает адаптацию в обществе и в быту.
                        </p>
                        <p>
                            Важным направлением работы Центра является работа с родственниками, чтобы восстановить гармоничный психоэмоциональный фон в семье и снять чрезмерную нагрузку.</p>
