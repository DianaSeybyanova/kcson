                            В Самарской области продолжают открываться Центры дневного пребывания. Они создаются благодаря реализации системы долговременного ухода (СДУ) за молодыми инвалидами и гражданами старшего поколения, которая является большой частью национального проекта &laquo;Демография&raquo;, инициированного президентом РФ Владимиром Путиным.</p>
                        <p>
                            Это новые социальные сервисы, &laquo;дневные пансионы&raquo;&nbsp;для людей, нуждающихся в уходе.<br />
