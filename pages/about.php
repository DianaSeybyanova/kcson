					</p>
					<ul>
						<li>
							выявлению, учету нуждающихся в социальных услугах детей и семей, оказавшихся в трудной жизненной ситуации, лиц пожилого возраста, инвалидов и иных граждан;</li>
						<li>
							осуществлению<a name="_GoBack"></a> мероприятий социального сопровождения данной категории граждан;</li>
						<li>
							профилактике безнадзорных детей, социального сиротства, защите прав несовершеннолетних, профилактике негативных явлений среди подростков и молодежи, укреплению института семьи, популяризации семейных ценностей, ответственного родительства;</li>
						<li>
							оказанию консультативной помощи в рамках компетенции Учреждения семье, имеющей под опекой детей (попечительством), приемной семье;</li>
						<li>
							социальной реабилитации детей с ограниченными возможностями здоровья, а также законных представителей таких детей;</li>
