						- ГБУ СО&laquo;Центр социального обслуживания граждан пожилого возраста и инвалидов городского округа Самара &laquo;Центральный&raquo;.</p>
					<p>
						<strong>Цель деятельности Учреждения:</strong><br />
						оказание социальных услуг детям и семьям, оказавшимся в трудной жизненной ситуации, лицам пожилого возраста, инвалидам и иным гражданам, признанными нуждающимися в социальном обслуживании, проживающим на территории Самарского округа.
					</p>
