					<p>
						На основании Постановления Правительства Самарской области от 20.04.2017 г. № 253, с 01.07.2017 г.было создано Государственное казенное учреждение Самарской области &laquo;Комплексный центр социального обслуживания населения Самарского округа&raquo; (далее &ndash; ГКУ СО &laquo;КЦСОН Самарского округа&raquo;) путем реорганизации следующих учреждений:<br />
